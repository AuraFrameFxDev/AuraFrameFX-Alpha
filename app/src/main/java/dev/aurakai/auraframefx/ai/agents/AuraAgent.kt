     *
     * Extracts the animation type from the request context, constructs an animation specification, and uses an AI client to generate the corresponding animation code. Returns a map containing the generated code, timing curve details, possible interaction states, and recommended performance optimizations.
     *
     * @param request The AI request specifying animation context and type.
     * @return A map with keys: "animation_code" (Kotlin code for the animation), "timing_curves" (timing curve details), "interaction_states" (possible animation states), and "performance_optimization" (suggested optimizations).
